<p align="justify"> 
Stunting adalah kondisi gagal tumbuh pada anak akibat kekurangan gizi kronis selama 1.000 hari pertama kehidupan, yang menyebabkan tinggi badan lebih pendek dari rata-rata anak seusianya. Penyebabnya termasuk asupan gizi buruk, sanitasi yang buruk, dan infeksi penyakit. Stunting mempengaruhi perkembangan fisik dan kognitif anak, menurunkan kemampuan belajar, serta meningkatkan risiko penyakit kronis di masa depan. Di Indonesia, prevalensi stunting mencapai 24,4% pada tahun 2022. Pencegahan dapat dilakukan melalui asupan gizi yang baik selama kehamilan, pemberian ASI eksklusif, MP-ASI bergizi, serta akses ke sanitasi dan layanan kesehatan yang memadai.
<br></br>
Definisi stunting dari berbagai sumber:
<br></br>
<b>1. World Health Organization (WHO)</b>: Stunting adalah kondisi di mana anak memiliki tinggi badan yang lebih rendah dari rata-rata untuk usianya akibat kekurangan gizi kronis, terutama pada 1.000 hari pertama kehidupan.
<br></br>
<b>2. United Nations Children's Fund (UNICEF)</b>: Stunting adalah kondisi pertumbuhan yang tidak optimal yang ditandai dengan tinggi badan anak yang kurang untuk usia, yang disebabkan oleh malnutrisi dan infeksi.
<br></br>
<b>3. Badan Kependudukan dan Keluarga Berencana Nasional (BKKBN)</b>: Stunting adalah keadaan gagal tumbuh pada anak yang ditandai dengan tinggi badan yang rendah dibandingkan dengan anak seusianya, biasanya akibat kurangnya asupan gizi yang adekuat.
<br></br>
<b>4. Kementerian Kesehatan Republik Indonesia</b>: Stunting adalah kondisi di mana anak mengalami pertumbuhan yang terhambat, yang ditunjukkan dengan tinggi badan yang lebih rendah dibandingkan dengan standar pertumbuhan anak seusianya.
<br></br>
<b>5. Food and Agriculture Organization (FAO)</b>: Stunting adalah bentuk malnutrisi yang ditandai dengan tinggi badan yang tidak mencukupi untuk usia, sering kali akibat dari pola makan yang tidak sehat dan penyakit.

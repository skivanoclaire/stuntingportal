 <b>Panduan Gizi untuk Ibu Hamil</b>
 <b>1. Trimester Pertama (0-12 Minggu)</b>
        <p>Pada trimester pertama, ibu hamil sering mengalami mual dan muntah (morning sickness), sehingga penting untuk mengonsumsi makanan yang mudah dicerna dan bergizi.</p>
        <ol style="list-style-type:disc; text-align: justify;">
            <li><strong>Asam Folat</strong>: Konsumsi makanan yang kaya akan asam folat seperti sayuran hijau, kacang-kacangan, dan jeruk untuk mencegah cacat tabung saraf pada janin.</li>
            <li><strong>Protein</strong>: Telur, ikan, ayam, tahu, dan tempe merupakan sumber protein yang penting untuk pertumbuhan sel janin.</li>
            <li><strong>Zat Besi</strong>: Sayuran hijau, daging merah, dan kacang-kacangan membantu memenuhi kebutuhan zat besi yang meningkat selama kehamilan.</li>
            <li><strong>Kalsium</strong>: Susu, yoghurt, keju, dan sayuran hijau adalah sumber kalsium yang dibutuhkan untuk perkembangan tulang dan gigi janin.</li>
        </ol>

        <b>2. Trimester Kedua (13-26 Minggu)</b>
        <p>Pada trimester kedua, pertumbuhan janin semakin pesat. Ibu hamil memerlukan lebih banyak energi dan nutrisi untuk mendukung perkembangan organ janin.</p>
        <ol style="list-style-type:disc; text-align: justify;">
            <li><strong>Karbohidrat Kompleks</strong>: Konsumsi nasi merah, gandum, oatmeal, dan kentang sebagai sumber energi.</li>
            <li><strong>Protein Hewani dan Nabati</strong>: Pastikan asupan protein meningkat dengan makan daging tanpa lemak, ikan, tahu, tempe, dan kacang-kacangan.</li>
            <li><strong>Vitamin D</strong>: Ibu hamil perlu memastikan tubuh mendapatkan cukup sinar matahari atau mengonsumsi ikan berlemak dan susu yang diperkaya vitamin D untuk mendukung penyerapan kalsium.</li>
            <li><strong>Serat</strong>: Konsumsi buah-buahan, sayuran, dan biji-bijian untuk mencegah sembelit, yang umum terjadi pada kehamilan.</li>
        </ol>

        <b>3. Trimester Ketiga (27-40 Minggu)</b>
        <p>Trimester ketiga adalah waktu pertumbuhan maksimal janin, sehingga ibu hamil memerlukan lebih banyak kalori dan gizi.</p>
        <ol style="list-style-type:disc; text-align: justify;">
            <li><strong>Omega-3</strong>: Konsumsi ikan berlemak seperti salmon dan tuna yang kaya omega-3, penting untuk perkembangan otak dan mata janin.</li>
            <li><strong>Vitamin C</strong>: Buah-buahan seperti jeruk, stroberi, dan pepaya membantu penyerapan zat besi dan menjaga sistem imun.</li>
            <li><strong>Kalsium dan Protein</strong>: Susu, yoghurt, keju, serta makanan kaya protein tetap harus menjadi bagian utama dari pola makan untuk menunjang perkembangan tulang dan otot janin.</li>
            <li><strong>Hidrasi</strong>: Minum air yang cukup (minimal 8 gelas per hari) sangat penting untuk menjaga keseimbangan cairan tubuh selama kehamilan.</li>
        </ol>

        <b>Panduan Gizi untuk Ibu Menyusui</b>

        <p>Setelah melahirkan, nutrisi tetap sangat penting untuk ibu menyusui. ASI mengandung semua nutrisi yang dibutuhkan bayi, sehingga pola makan ibu harus cukup untuk memenuhi kebutuhan gizi ibu dan bayi.</p>

        <b>1. Nutrisi Penting untuk Ibu Menyusui</b>
        <p>Penting untuk mengonsumsi makanan bergizi seimbang yang mencakup semua kelompok makanan.</p>
        <ol style="list-style-type:disc; text-align: justify;">
            <li><strong>Kalori Tambahan</strong>: Ibu menyusui memerlukan sekitar 500 kalori ekstra setiap hari untuk produksi ASI yang optimal.</li>
            <li><strong>Protein</strong>: Konsumsi lebih banyak protein melalui telur, ikan, ayam, kacang-kacangan, dan tahu.</li>
            <li><strong>Kalsium dan Vitamin D</strong>: Susu, yoghurt, keju, dan sayuran hijau membantu menggantikan kalsium yang diserap oleh ASI. Pastikan juga mendapatkan cukup vitamin D.</li>
            <li><strong>Omega-3</strong>: Ikan berlemak seperti salmon dan sarden penting untuk perkembangan otak bayi melalui ASI.</li>
            <li><strong>Serat</strong>: Buah-buahan, sayuran, dan biji-bijian membantu mencegah sembelit yang sering terjadi setelah melahirkan.</li>
        </ol>

        <b>2. Cairan dan Hidrasi</b>
        <p>Ibu menyusui perlu minum lebih banyak air untuk menjaga produksi ASI. Pastikan mengonsumsi minimal 8-10 gelas air per hari.</p>

        <b>3. Makanan yang Harus Dihindari</b>
        <p>Terdapat beberapa makanan yang perlu dihindari selama menyusui, karena dapat mempengaruhi bayi melalui ASI.</p>
        <ol style="list-style-type:disc; text-align: justify;">
            <li><strong>Kafein</strong>: Batasi konsumsi kopi dan teh, karena kafein dapat menyebabkan bayi menjadi gelisah atau sulit tidur.</li>
            <li><strong>Alkohol</strong>: Hindari alkohol sepenuhnya, karena dapat masuk ke dalam ASI dan mempengaruhi perkembangan bayi.</li>
            <li><strong>Makanan Pedas atau Berbumbu Kuat</strong>: Beberapa bayi mungkin sensitif terhadap makanan pedas atau yang beraroma kuat, seperti bawang putih, yang dikonsumsi ibu.</li>
        </ol>

        <p><strong>Panduan gizi ini bertujuan untuk memastikan ibu hamil dan menyusui mendapatkan nutrisi yang tepat</strong> untuk kesehatan mereka dan bayi yang mereka rawat.</p>
  
 

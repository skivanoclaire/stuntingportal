<b>1. Kekurangan Asupan Gizi</b>
<ol style="list-style-type:disc; text-align: justify;">
	<li>
    <b>Nutrisi Ibu Hamil</b>: Ibu yang tidak mendapatkan nutrisi yang cukup selama kehamilan dapat melahirkan bayi dengan berat badan rendah, yang merupakan faktor risiko utama stunting. Nutrisi penting seperti protein, zat besi, dan asam folat sangat diperlukan selama kehamilan.
    <b>Pola Makan Anak</b>: Setelah lahir, anak perlu mendapatkan asupan gizi yang cukup, terutama dalam 1.000 hari pertama kehidupannya. Kekurangan makanan bergizi, seperti sayuran, buah-buahan, dan sumber protein, dapat menyebabkan stunting.
	</li>
</ol>
<b>2. Sanitasi dan Kebersihan yang Buruk</b>
<ol style="list-style-type:disc; text-align: justify;">
	<li>
    <b>Akses Terhadap Air Bersih</b>: Sanitasi yang buruk dan kurangnya akses terhadap air bersih meningkatkan risiko infeksi, seperti diare, yang dapat mengganggu penyerapan gizi. Anak yang sering sakit tidak dapat menyerap nutrisi secara optimal.
    <b>Kebersihan Lingkungan</b>: Lingkungan yang kotor dapat menjadi tempat berkembang biaknya patogen yang menyebabkan penyakit. Hal ini berdampak pada kesehatan anak dan dapat menyebabkan stunting.
	</li>
</ol>
<b>3. Infeksi dan Penyakit</b>
<ol style="list-style-type:disc; text-align: justify;">
	<li>
    <b>Infeksi Saluran Cerna</b>: Penyakit infeksi, terutama infeksi saluran cerna, dapat menghambat penyerapan nutrisi. Ketika anak sering mengalami diare atau infeksi lainnya, tubuhnya kehilangan banyak nutrisi yang diperlukan untuk pertumbuhan.
    <b>Penyakit Kronis</b>: Anak yang memiliki penyakit kronis atau masalah kesehatan jangka panjang juga berisiko lebih tinggi mengalami stunting karena tubuh mereka memerlukan lebih banyak energi dan nutrisi untuk melawan penyakit.
</li>
</ol>
<b>4. Pola Asuh dan Pengetahuan Gizi</b>
<ol style="list-style-type:disc; text-align: justify;">
	<li>
    <b>Kurangnya Pengetahuan</b>: Orang tua yang tidak memiliki pengetahuan tentang pentingnya gizi seimbang, penyusuan ASI eksklusif, dan makanan pendamping ASI (MP-ASI) yang tepat dapat berkontribusi pada risiko stunting.
   <b> Praktik Pemberian Makan</b>: Kebiasaan memberi makan yang tidak tepat, seperti memberi makanan yang tidak bervariasi atau tidak sesuai dengan kebutuhan gizi anak, dapat menyebabkan kekurangan gizi.
</li>
</ol>
<b>5. Faktor Sosial Ekonomi</b>
<ol style="list-style-type:disc; text-align: justify;">
	<li>
    <b>Kondisi Ekonomi</b>: Keluarga dengan kondisi ekonomi yang rendah seringkali tidak mampu membeli makanan bergizi atau mendapatkan layanan kesehatan yang memadai. Ketidakstabilan ekonomi juga berdampak pada ketahanan pangan.
    <b>Pendidikan Orang Tua</b>: Tingkat pendidikan orang tua, terutama ibu, berhubungan erat dengan pengetahuan tentang gizi dan kesehatan. Orang tua yang kurang teredukasi cenderung tidak memahami pentingnya gizi bagi pertumbuhan anak.
</li>
</ol>
<b>6. Faktor Genetik dan Keturunan</b>
<ol style="list-style-type:disc; text-align: justify;">
	<li>
   <b> Faktor Genetik</b>: Walaupun lebih sedikit berperan, faktor genetik dan keturunan juga dapat mempengaruhi pertumbuhan fisik anak. Anak dari orang tua dengan tinggi badan di bawah rata-rata mungkin berisiko lebih tinggi mengalami stunting.
</li>
</ol>

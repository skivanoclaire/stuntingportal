        <b>1. Panduan Gizi untuk Anak Usia 0-6 Bulan</b>
        <p><strong>ASI Eksklusif</strong>: Pada usia ini, bayi harus mendapatkan <strong>Air Susu Ibu (ASI) eksklusif</strong> tanpa tambahan makanan atau minuman lain, termasuk air. ASI memenuhi semua kebutuhan nutrisi yang diperlukan bayi untuk tumbuh optimal selama enam bulan pertama kehidupannya.</p>
        <ol style="list-style-type:disc; text-align: justify;">
            <li><strong>Frekuensi Menyusui</strong>: Sekitar 8-12 kali per hari sesuai kebutuhan bayi.</li>
            <li><strong>Manfaat ASI</strong>: Mengandung zat gizi esensial, seperti protein, lemak, karbohidrat, serta vitamin dan mineral, yang mendukung pertumbuhan fisik dan perkembangan otak.</li>
        </ol>

        <b>2. Panduan Gizi untuk Anak Usia 6-12 Bulan</b>
        <p>Pada usia ini, <strong>Makanan Pendamping ASI (MP-ASI)</strong> harus mulai diberikan untuk melengkapi kebutuhan gizi bayi, di samping tetap memberikan ASI. Makanan yang diberikan harus bergizi seimbang, mudah dicerna, dan disesuaikan dengan kemampuan anak dalam mengunyah dan menelan.</p>
        <ol style="list-style-type:disc; text-align: justify;">
            <li><strong>Karbohidrat</strong>: Bubur nasi, kentang, ubi, jagung, atau sereal bayi.</li>
            <li><strong>Protein</strong>: Daging ayam, daging sapi, ikan (pastikan tidak ada duri), tahu, tempe, telur (bagian kuning telur diberikan secara bertahap).</li>
            <li><strong>Sayur-sayuran</strong>: Bayam, wortel, labu, brokoli, yang dihaluskan atau dipotong kecil.</li>
            <li><strong>Buah-buahan</strong>: Pisang, pepaya, alpukat, apel, dan pir yang telah dihaluskan.</li>
            <li><strong>Lemak Sehat</strong>: Minyak zaitun atau mentega dapat ditambahkan dalam makanan bayi untuk tambahan kalori.</li>
        </ol>

        <p><strong>Porsi dan Frekuensi</strong>:</p>
        <ol style="list-style-type:disc; text-align: justify;">
            <li><strong>Pemberian MP-ASI</strong>: 2-3 kali sehari ditambah 1-2 kali camilan.</li>
            <li><strong>Porsi</strong>: Dimulai dengan 2-3 sendok makan per pemberian, kemudian ditingkatkan secara bertahap sesuai kemampuan bayi.</li>
        </ol>

        <b>3. Panduan Gizi untuk Anak Usia 1-2 Tahun</b>
        <p>Pada usia ini, kebutuhan gizi anak meningkat karena aktivitas fisiknya mulai bertambah. Anak juga mulai belajar makan sendiri. ASI masih dianjurkan hingga usia 2 tahun, namun porsi makanan padat harus lebih banyak.</p>
        <ol style="list-style-type:disc; text-align: justify;">
            <li><strong>Karbohidrat</strong>: Nasi lembek, bubur, roti, kentang, atau pasta.</li>
            <li><strong>Protein Hewani dan Nabati</strong>:
                <ol style="list-style-type:disc; text-align: justify;">
                    <li>Daging ayam, ikan, daging sapi tanpa lemak.</li>
                    <li>Tahu, tempe, kacang-kacangan, dan telur.</li>
                </ol>
            </li>
            <li><strong>Sayuran dan Buah-buahan</strong>: Sayur seperti wortel, bayam, brokoli, buncis, dan tomat.</li>
            <li><strong>Susu dan Produk Olahan Susu</strong>: Susu sapi, yoghurt, atau keju dalam porsi kecil.</li>
            <li><strong>Lemak Sehat</strong>: Minyak zaitun atau minyak kelapa untuk memasak, atau mentega.</li>
        </ol>

        <p><strong>Porsi dan Frekuensi</strong>:</p>
        <ol style="list-style-type:disc; text-align: justify;">
            <li><strong>3 kali makan utama</strong> dan <strong>2 kali camilan sehat</strong> per hari.</li>
            <li>Setiap porsi makanan padat sekitar <strong>1/2 mangkuk kecil</strong> untuk nasi, dan <strong>3-4 sendok makan</strong> untuk lauk-pauk.</li>
        </ol>

        <b>4. Panduan Gizi untuk Anak Usia 2-5 Tahun</b>
        <p>Pada usia prasekolah, anak sudah mulai memiliki pola makan yang lebih mirip dengan orang dewasa, namun tetap membutuhkan porsi yang lebih kecil dan makanan yang lebih mudah dicerna. Penting untuk memperkenalkan berbagai jenis makanan agar anak terbiasa dengan rasa dan tekstur yang berbeda.</p>
        <ol style="list-style-type:disc; text-align: justify;">
            <li><strong>Karbohidrat</strong>: Nasi, roti gandum, kentang, pasta, atau mi.</li>
            <li><strong>Protein</strong>: Daging tanpa lemak, ikan, telur, tahu, tempe, dan kacang-kacangan. Anak-anak pada usia ini membutuhkan <strong>sekitar 13 gram protein per hari</strong>.</li>
            <li><strong>Buah dan Sayur</strong>: Pastikan anak mengonsumsi minimal <strong>5 porsi buah dan sayur</strong> setiap hari.</li>
            <li><strong>Produk Olahan Susu</strong>: Susu, yoghurt, dan keju. Susu sangat penting untuk kalsium, namun batasi jumlahnya menjadi <strong>dua gelas sehari</strong> untuk menghindari kelebihan kalori.</li>
            <li><strong>Lemak Sehat</strong>: Gunakan minyak zaitun atau minyak kelapa sebagai sumber lemak sehat.</li>
        </ol>

        <p><strong>Porsi dan Frekuensi</strong>:</p>
        <ol style="list-style-type:disc; text-align: justify;">
            <li><strong>3 kali makan utama</strong> yang seimbang dan <strong>2 kali camilan sehat</strong>.</li>
            <li>Porsi karbohidrat sekitar <strong>1 mangkuk nasi</strong>, dengan <strong>1 potong daging</strong> (sekitar 30-50 gram), dan <strong>sayur secukupnya</strong>.</li>
            <li>Berikan camilan berupa buah-buahan segar, yoghurt, atau roti gandum dengan selai kacang.</li>
        </ol>

        <b>5. Panduan Gizi untuk Anak Usia 5-12 Tahun (Usia Sekolah)</b>
        <p>Anak usia sekolah membutuhkan energi dan gizi yang lebih banyak karena aktivitas fisik yang tinggi dan perkembangan otak yang terus berlangsung. Ini juga masa penting untuk membentuk kebiasaan makan sehat.</p>
        <ol style="list-style-type:disc; text-align: justify;">
            <li><strong>Karbohidrat</strong>: Nasi, roti gandum, oatmeal, kentang, dan pasta.</li>
            <li><strong>Protein</strong>: Daging ayam, ikan, telur, tahu, tempe, dan kacang-kacangan. Anak pada usia ini membutuhkan sekitar <strong>19-34 gram protein</strong> per hari tergantung usia.</li>
            <li><strong>Buah dan Sayur</strong>: Anak-anak harus mendapatkan minimal <strong>5 porsi buah dan sayuran</strong> per hari.</li>
            <li><strong>Produk Susu</strong>: Konsumsi <strong>2-3 porsi susu atau produk olahan susu</strong> setiap hari untuk memastikan kebutuhan kalsium tercukupi.</li>
            <li><strong>Lemak Sehat</strong>: Lemak dari minyak nabati, kacang-kacangan, dan ikan berlemak seperti tuna, lele, nila, kembung, gabus, bawal atau bandeng.</li>
        </ol>

        <p><strong>Porsi dan Frekuensi</strong>:</p>
        <ol style="list-style-type:disc; text-align: justify;">
            <li><strong>3 kali makan utama</strong> yang seimbang dan <strong>2 kali camilan sehat</strong>.</li>
            <li>Porsi karbohidrat untuk makan utama sekitar <strong>1-1,5 mangkuk nasi</strong>, dengan <strong>1 potong daging</strong> (sekitar 50-75 gram), dan <strong>sayuran yang cukup</strong>.</li>
            <li>Camilan dapat berupa buah-buahan segar, yoghurt, kacang-kacangan, atau roti gandum.</li>
        </ol>

        <b>Tips Tambahan</b>
        <ol style="list-style-type:disc; text-align: justify;">
            <li>Berikan makanan dengan porsi kecil tetapi sering untuk menjaga nafsu makan anak.</li>
            <li>Ajari anak makan di meja dan bukan sambil bermain atau menonton TV.</li>
            <li>Anak perlu diajarkan untuk makan perlahan guna menghindari masalah pencernaan.</li>
        </ol>

        <p><strong>Panduan gizi ini dirancang untuk membantu orang tua dan pengasuh</strong> dalam memastikan anak-anak mendapatkan nutrisi yang optimal untuk mendukung pertumbuhan mereka.</p>
 

            <div class="container">
Pemerintah Indonesia berkomitmen untuk menurunkan angka stunting melalui berbagai program dan kebijakan strategis yang melibatkan lintas sektor. Penanganan stunting merupakan prioritas utama yang diwujudkan melalui pendekatan komprehensif. Berikut adalah solusi yang dilakukan pemerintah untuk mengatasi stunting:
<br></br>
<b>1. Peningkatan Gizi Ibu Hamil dan Anak Balita</b>
<ol style="list-style-type:disc; text-align: justify;">
	<li>
	<b>Program Suplementasi Gizi</b>: Pemerintah melalui Kementerian Kesehatan memberikan suplemen gizi seperti zat besi, asam folat, dan vitamin A bagi ibu hamil dan balita di posyandu dan puskesmas. Hal ini bertujuan untuk mencegah kekurangan gizi yang dapat berdampak pada pertumbuhan anak.
	</li>
	<li>
	<b>Penyediaan MP-ASI yang Bergizi</b>: Pemerintah bekerja sama dengan sektor swasta dan komunitas untuk menyediakan Makanan Pendamping Air Susu Ibu (MP-ASI) yang berkualitas, terutama bagi keluarga prasejahtera. Makanan ini disalurkan melalui posyandu dan program gizi masyarakat
	</li>
</ol>
<b>2. Kampanye Gizi Seimbang dan Edukasi Kesehatan</b>
<ol style="list-style-type:disc; text-align: justify;">
	<li>
	<b>Kampanye Gizi Nasional</b>: Pemerintah menjalankan kampanye gizi seimbang secara nasional untuk meningkatkan kesadaran masyarakat mengenai pentingnya nutrisi yang baik bagi ibu hamil, menyusui, dan anak-anak. Edukasi ini dilakukan melalui media massa, media sosial, dan penyuluhan di desa-desa
	</li>
	<li>
	<b>Penyuluhan Gizi di Posyandu</b>: Posyandu di seluruh Indonesia menjadi pusat penyuluhan gizi yang memberikan edukasi kepada ibu-ibu mengenai pentingnya pemberian ASI eksklusif selama 6 bulan pertama, pemberian MP-ASI yang tepat, serta pencegahan infeksi dan penyakit
	</li>
</ol>
<b>3. Perbaikan Akses Sanitasi dan Air Bersih</b>
<ol style="list-style-type:disc; text-align: justify;">
	<li>
	<b>Program Sanitasi Total Berbasis Masyarakat (STBM)</b>: Melalui program ini, pemerintah memastikan bahwa setiap rumah tangga memiliki akses terhadap sanitasi yang layak, seperti fasilitas toilet yang memadai dan akses air bersih. Pemerintah juga mempromosikan perilaku hidup bersih dan sehat (PHBS) untuk mencegah penyebaran penyakit yang dapat memperburuk kondisi stunting.
	</li>
	<li>
	<b>Penyediaan Infrastruktur Air Bersih</b>: Pemerintah terus memperluas akses masyarakat terhadap air bersih dengan membangun infrastruktur air bersih di daerah-daerah terpencil dan rawan stunting. Upaya ini dilakukan untuk mencegah penyakit infeksi yang dapat menghambat penyerapan gizi pada anak.
	</li>
</ol>
<b>4. Peningkatan Layanan Kesehatan dan Imunisasi</b>
<ol style="list-style-type:disc; text-align: justify;">
	<li>
	<b>Layanan Kesehatan Gratis untuk Ibu dan Anak</b>:Pemerintah memastikan ibu hamil dan balita mendapatkan akses layanan kesehatan gratis di puskesmas melalui Jaminan Kesehatan Nasional (JKN) dan Kartu Indonesia Sehat (KIS). Pemeriksaan rutin bagi ibu hamil serta pemantauan tumbuh kembang anak dilakukan secara berkala.
	</li>
	<li>
	<b>Program Imunisasi Nasional</b>: Untuk mencegah penyakit yang dapat memperburuk kondisi stunting, pemerintah terus menjalankan program imunisasi bagi bayi dan anak-anak. Imunisasi dasar lengkap sangat penting untuk melindungi anak dari penyakit infeksi.	
	</li>
	</ol>
<b>5. Penguatan Sistem Pemantauan dan Intervensi Gizi di 1.000 Hari Pertama Kehidupan</b>
<ol style="list-style-type:disc; text-align: justify;">
	<li>
	<b>Pemantauan Tumbuh Kembang di Posyandu</b>: Pemerintah meningkatkan peran Posyandu dalam memantau tumbuh kembang anak, terutama dalam 1.000 hari pertama kehidupan. Pemantauan berat badan, tinggi badan, serta status gizi dilakukan secara berkala, dan intervensi dilakukan jika ditemukan risiko stunting.
	</li>
	<li>
	<b>Intervensi Gizi Spesifik pada 1.000 Hari Pertama</b>: Pemerintah memberikan perhatian khusus pada 1.000 hari pertama kehidupan, mulai dari masa kehamilan hingga anak berusia 2 tahun, dengan memberikan paket gizi lengkap dan pendampingan intensif bagi ibu hamil dan menyusui.
	</li>
</ol>
<b>6. Program Perlindungan Sosial untuk Keluarga Berisiko</b>
<ol style="list-style-type:disc; text-align: justify;">
	<li>
	<b>Program Keluarga Harapan (PKH)</b>: Melalui PKH, pemerintah memberikan bantuan tunai bersyarat kepada keluarga miskin yang memiliki ibu hamil dan anak balita. Keluarga penerima PKH harus memenuhi syarat tertentu, seperti memeriksakan kesehatan ibu hamil dan anak ke fasilitas kesehatan, serta menyekolahkan anak.	
	</li>
	<li>
	<b>Bantuan Pangan Non-Tunai (BPNT)</b>: Pemerintah juga menyediakan Bantuan Pangan Non-Tunai berupa bahan makanan bergizi bagi keluarga miskin untuk memastikan kebutuhan gizi keluarga tercukupi. Program ini bertujuan agar keluarga dapat membeli bahan makanan berkualitas tinggi seperti beras, telur, dan sumber protein lainnya.
	</li>
</ol>
<b>7. Kemitraan Lintas Sektor dan Kebijakan Terintegrasi</b>
<ol style="list-style-type:disc; text-align: justify;">
	<li>
	<b>Kolaborasi Antar-Kementerian</b>: Penanganan stunting dilakukan melalui kolaborasi antar-kementerian, seperti Kementerian Kesehatan, Kementerian Pendidikan, Kementerian Pekerjaan Umum dan Perumahan Rakyat, serta Kementerian Sosial. Setiap kementerian berperan dalam menyediakan layanan yang mendukung penurunan angka stunting, seperti penyediaan infrastruktur sanitasi, pendidikan gizi, dan bantuan sosial.
	</li>
	<li>
	<b>Program Desa Bebas Stunting</b>: Pemerintah meluncurkan Program Desa Bebas Stunting yang melibatkan berbagai pihak, termasuk perangkat desa, untuk memastikan setiap desa memiliki program yang jelas dalam pencegahan dan penanganan stunting. Ini meliputi peningkatan akses layanan kesehatan, edukasi gizi, dan perbaikan sanitasi di tingkat desa.
	</li>
</ol>
<b>8. Kampanye Kesadaran Stunting di Masyarakat</b>
<ol style="list-style-type:disc; text-align: justify;">
	<li>
	<b>Kampanye Publik Nasional</b>: Pemerintah terus melakukan kampanye publik berskala nasional untuk meningkatkan kesadaran masyarakat tentang bahaya stunting dan pentingnya pencegahan sejak dini. Kampanye ini menggunakan media cetak, media elektronik, dan media sosial, serta melibatkan tokoh masyarakat dan pemuka agama dalam penyuluhan.
	</li>
	<li>
	<b>Pemberdayaan Kader Posyandu</b>: Kader posyandu dilatih untuk melakukan penyuluhan dan pemantauan tumbuh kembang anak di desa-desa. Mereka menjadi ujung tombak dalam memberikan informasi tentang pencegahan stunting di masyaraka
	</li>
</ol>
